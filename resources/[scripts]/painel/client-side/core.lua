-----------------------------------------------------------------------------------------------------------------------------------------
-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp","lib/Tunnel")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
Lil = {}
Tunnel.bindInterface("painel",Lil)
vSERVER = Tunnel.getInterface("painel")
-----------------------------------------------------------------------------------------------------------------------------------------
-- PAINEL:OPENED
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNetEvent("painel:Opened")
AddEventHandler("painel:Opened",function()
	local Departmenty = vSERVER.Department()
	if Departmenty then
		SetNuiFocus(true,true)
		TransitionToBlurred(1000)
		SetCursorLocation(0.5,0.5)
		TriggerEvent("dynamic:Close")
		TriggerEvent("hud:Active",false)
		SendNUIMessage({ Action = "Open", Payload = vSERVER.Player() })
	end
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- PAINEL:NOTIFY
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNetEvent("painel:Notify")
AddEventHandler("painel:Notify",function(Title,Message,Type)
	SendNUIMessage({ Action = "Notify", Payload = { Title = Title, Message = Message, Type = Type } })
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- CLOSE
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("Close",function(Data,Callback)
	SetNuiFocus(false,false)
	SetCursorLocation(0.5,0.5)
	TransitionFromBlurred(1000)
	TriggerEvent("hud:Active",true)

	Callback("Ok")
end)
