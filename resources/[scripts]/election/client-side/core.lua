-----------------------------------------------------------------------------------------------------------------------------------------
-- DYNAMIC:OPEN
-----------------------------------------------------------------------------------------------------------------------------------------
AddEventHandler("election:Open",function()
	if lib.callback.await("election:Check",false) then
		SetNuiFocus(true,true)
		TransitionToBlurred(1000)
		TriggerEvent("hud:Active",false)
		SendNUIMessage({ Action = "Open" })
	end
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- CLOSE
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("Close",function(Data,Callback)
	SetNuiFocus(false,false)
	TransitionFromBlurred(1000)
	TriggerEvent("hud:Active",true)

	Callback(true)
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONFIG
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("Config",function(Data,Callback)
	Callback(lib.callback.await("election:Config",false))
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- VOTE
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("Vote",function(Data,Callback)
	Callback(lib.callback.await("election:Update",false,Data.Candidate,Data.Vote))
end)
