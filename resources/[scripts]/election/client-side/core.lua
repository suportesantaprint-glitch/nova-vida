-- THREADSERVERSTART
-----------------------------------------------------------------------------------------------------------------------------------------
CreateThread(function()
	for Number,v in pairs(Locations) do
		exports.target:AddCircleZone("Election:"..Number,v,0.25,{
			name = "Election:"..Number,
			heading = 0.0,
			useZ = true
		},{
			shop = Number,
			Distance = 1.25,
			options = {
				{
					event = "election:Open",
					tunnel = "client",
					label = "Votar"
				}
			}
		})
	end
end)
