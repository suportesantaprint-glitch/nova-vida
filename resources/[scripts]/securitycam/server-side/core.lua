-----------------------------------------------------------------------------------------------------------------------------------------
-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
Lil = {}
Tunnel.bindInterface("securitycam",Lil)
-----------------------------------------------------------------------------------------------------------------------------------------
-- VARIABLES
-----------------------------------------------------------------------------------------------------------------------------------------
local Objects = {}
local InativeTime = 0
local Active = {}
-----------------------------------------------------------------------------------------------------------------------------------------
-- THREADSERVERSTART
-----------------------------------------------------------------------------------------------------------------------------------------
CreateThread(function()
	Wait(1000)
	TriggerClientEvent("objects:Table",-1,Objects)
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTIONS
-----------------------------------------------------------------------------------------------------------------------------------------
function Lil.Connections()
	return InativeTime <= os.time()
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- TAKEITEM
-----------------------------------------------------------------------------------------------------------------------------------------
function Lil.TakeItem()
	local source = source
	local Passport = vRP.Passport(source)
	if Passport and not Active[Passport] then
		Active[Passport] = true

		if vRP.ConsultItem(Passport,HackerItem,1) and vRP.TakeItem(Passport,HackerItem,1,true) then
			Active[Passport] = nil
			return true
		else
			Active[Passport] = nil
			return false
		end
	end
	return false
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- INATIVE
-----------------------------------------------------------------------------------------------------------------------------------------
function Lil.Inative()
	local source = source
	local Passport = vRP.Passport(source)
	if Passport and not Active[Passport] then
		Active[Passport] = true

		InativeTime = os.time() + (InativeMinutes * 60)
		
		Active[Passport] = nil
	end
end
