-----------------------------------------------------------------------------------------------------------------------------------------
-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp","lib/Tunnel")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
vSERVER = Tunnel.getInterface("referrals")
-----------------------------------------------------------------------------------------------------------------------------------------
-- REFERRALS:OPEN
-----------------------------------------------------------------------------------------------------------------------------------------
AddEventHandler("referrals:Open",function()
	if vSERVER.Check() then
		SetNuiFocus(true,true)
		TransitionToBlurred(1000)
		SetCursorLocation(0.5,0.5)
		TriggerEvent("hud:Active",false)
		SendNUIMessage({ Action = "Open", Payload = Origin })
	end
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONFIRM
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("Confirm",function(Data,Callback)
	SetNuiFocus(false,false)
	SetCursorLocation(0.5,0.5)
	TransitionFromBlurred(1000)
	TriggerEvent("hud:Active",true)

	Callback(vSERVER.Confirm(Data.Origin,Data.Code))
end)